// `workspace_state_loader` — the replacement for the previous plan's
// "orchestrator bridge". Three `pub fn` operations that move per-modlist
// state between the persisted `ModlistWorkspaceState`
// (`modlists/<id>/workspace.json`) and the orchestrator's owned
// `WizardState`:
//
//   1. `populate_wizard_state_from_workspace` — workspace → `WizardState`,
//      called on workspace open / modlist swap. **Real Step 2 + Step 3
//      reconstruction** (not `game_install`-only — that was the PR#11
//      failure this loader exists to not repeat). It marks the persisted
//      `ComponentRef`s `checked` + `selected_order` on the scanned Step 2
//      mods, re-derives the per-mod tri-state, and rebuilds the Step 3
//      ordered item list via BIO's own canonical
//      `bio::app::controller::step3_sync::build_step3_items` (the exact
//      function BIO uses to keep Step 3 in sync with Step 2 selections).
//
//   2. `extract_workspace_state_from_wizard` — `WizardState` → workspace,
//      the inverse. Called on save-draft / nav-away / debounce (those
//      callers land in Runs 2/4 — extract ships unwired this run, which is
//      correct).
//
//   3. `sync_paths_from_settings` — a one-time re-assert of the canonical
//      `Step1Settings` path/tool fields into `wizard_state.step1`, called on
//      workspace open (from `populate`). Per-frame is unnecessary: Settings
//      → Paths edits the *same* in-memory `wizard_state.step1`, so paths are
//      live by construction. Pure copy; touches nothing else.
//
// Uses BIO's `pub` / `pub(crate)` API only (same-crate reachable per the
// Phase-1 lib+bin split). No BIO source modification; no field is private;
// no method is added.
//
// **Step 2 expand-state note.** `ModlistWorkspaceState.expand_state` has no
// `WizardState` counterpart: BIO's Step 2 tree mod/parent expand/collapse is
// `egui::CollapsingHeader` *frame memory*, not a `WizardState` field. The
// canonical persisted store for it is `ModlistWorkspaceState` itself, so the
// loader carries the map through on extract (preserving whatever was loaded)
// and is a no-op for it on populate. This is an architectural fact, not the
// order-reconstruction make-or-break (Step 3 *group* collapse, which DOES
// live on `WizardState`, round-trips for real). The plan (P6.T1) phrases this
// as the `expand_state`-"equivalent" and treats workspace-model field
// alignment as free.
//
// C5 — never invoked during install: the rail-nav lock (Phase 7 P7.T9b)
// prevents nav-into-a-different-workspace mid-install, so populate is never
// reached with a running install.
//
// SPEC: §13.1 (per-modlist workspace state), §13.14 (persistence timing),
//       §5.1 (game choice immutable per workspace).

// rationale: the numeric parses are component-id strings → i64; on the rare
// non-numeric synthetic row we skip rather than panic (handled explicitly).
