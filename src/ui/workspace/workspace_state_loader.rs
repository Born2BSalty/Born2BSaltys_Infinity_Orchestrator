    /// The make-or-break test: populate must reconstruct the Step 2 checked
    /// state AND the Step 3 ordered item list from a persisted order — not
    /// just `game_install`.
    #[test]
    fn populate_reconstructs_step2_and_step3_from_order() {
        let mut ws = WizardState::default();
        // Two scanned mods, three components total, none checked yet.
        ws.step2.bgee_mods = vec![
            mod_state(
                "EEFixPack",
                "EEFIXPACK/EEFIXPACK.TP2",
                vec![comp("0", "Core Fixes"), comp("2", "Game Text Update")],
            ),
            mod_state(
                "BG1UB",
                "BG1UB/SETUP-BG1UB.TP2",
                vec![comp("0", "Restored")],
            ),
        ];

        // Persisted order: BG1UB #0 first, then EEFixPack #2 (deliberately
        // not the scan order, to prove order is honored).
        let mut workspace = ModlistWorkspaceState::default();
        workspace.order_bgee = vec![
            ComponentRef {
                tp2: "BG1UB/SETUP-BG1UB.TP2".to_string(),
                id: 0,
                language: 0,
            },
            ComponentRef {
                tp2: "EEFIXPACK/EEFIXPACK.TP2".to_string(),
                id: 2,
                language: 0,
            },
        ];

        populate_wizard_state_from_workspace(
            &workspace,
            &entry(Game::BGEE),
            &SettingsStore::new_default(),
            &mut ws,
        );

        // game_install set.
        assert_eq!(ws.step1.game_install, "BGEE");

        // Step 2: exactly the two ordered components are checked with the
        // persisted order; the third (EEFixPack #0) stays unchecked.
        let eefix = &ws.step2.bgee_mods[0];
        let bg1ub = &ws.step2.bgee_mods[1];
        assert!(!eefix.components[0].checked, "EEFixPack #0 must stay off");
        assert!(eefix.components[1].checked, "EEFixPack #2 must be on");
        assert_eq!(eefix.components[1].selected_order, Some(2));
        assert!(bg1ub.components[0].checked, "BG1UB #0 must be on");
        assert_eq!(bg1ub.components[0].selected_order, Some(1));
        // Mod tri-state re-derived.
        assert!(eefix.checked);
        assert!(bg1ub.checked);

        // Step 3: real reconstruction — BIO's build_step3_items produces
        // parent rows + the two component rows in persisted order.
        let leaves: Vec<&Step3ItemState> = ws
            .step3
            .bgee_items
            .iter()
            .filter(|i| !i.is_parent)
            .collect();
        assert_eq!(leaves.len(), 2, "two component rows expected");
        // BG1UB #0 (selected_order 1) before EEFixPack #2 (selected_order 2).
        assert_eq!(leaves[0].tp_file, "BG1UB/SETUP-BG1UB.TP2");
        assert_eq!(leaves[0].component_id, "0");
        assert_eq!(leaves[1].tp_file, "EEFIXPACK/EEFIXPACK.TP2");
        assert_eq!(leaves[1].component_id, "2");
        // At least one synthetic parent header row was generated.
        assert!(ws.step3.bgee_items.iter().any(|i| i.is_parent));
        // next_selection_order is past the largest restored order.
        assert_eq!(ws.step2.next_selection_order, 3);
    }

    /// extract must be the inverse: a populated WizardState extracts back to
    /// an equivalent order (parent rows dropped, component refs preserved in
    /// order).
    #[test]
    fn extract_is_inverse_of_populate_for_order() {
        let mut ws = WizardState::default();
        ws.step2.bgee_mods = vec![mod_state(
            "EEFixPack",
            "EEFIXPACK/EEFIXPACK.TP2",
            vec![comp("0", "Core"), comp("2", "GTU")],
        )];
        let mut workspace = ModlistWorkspaceState::default();
        workspace.order_bgee = vec![
            ComponentRef {
                tp2: "EEFIXPACK/EEFIXPACK.TP2".to_string(),
                id: 0,
                language: 0,
            },
            ComponentRef {
                tp2: "EEFIXPACK/EEFIXPACK.TP2".to_string(),
                id: 2,
                language: 0,
            },
        ];
        populate_wizard_state_from_workspace(
            &workspace,
            &entry(Game::BGEE),
            &SettingsStore::new_default(),
            &mut ws,
        );

        let extracted = extract_workspace_state_from_wizard(&ws, &workspace);
        assert_eq!(extracted.order_bgee, workspace.order_bgee);
    }

    /// The #1-fix `dev_scanned_mods_folder` has no `WizardState` counterpart;
    /// `extract` must carry it through from `prior` unchanged so an unrelated
    /// save (save-draft / debounce) never drops the dev-scan recording (the
    /// same non-drop guarantee `expand_state` / `prompt_overrides` get).
    #[test]
    fn extract_carries_dev_scan_folder_through() {
        let ws = WizardState::default();
        let prior = ModlistWorkspaceState {
            dev_scanned_mods_folder: Some(r"D:\corpus".to_string()),
            ..Default::default()
        };
        let extracted = extract_workspace_state_from_wizard(&ws, &prior);
        assert_eq!(
            extracted.dev_scanned_mods_folder.as_deref(),
            Some(r"D:\corpus"),
            "extract must not drop the dev-scan folder"
        );
    }

    /// Step 3 group-collapse round-trips through the prefixed flat map.
    #[test]
    fn step3_collapse_round_trips() {
        let mut ws = WizardState::default();
        ws.step3.bgee_collapsed_blocks = vec!["BLOCK_A".to_string(), "BLOCK_B".to_string()];
        ws.step3.bg2ee_collapsed_blocks = vec!["BLOCK_C".to_string()];

        let extracted = extract_workspace_state_from_wizard(&ws, &ModlistWorkspaceState::default());
        // Stored with <TAB>:: prefix so tabs don't collide.
        assert_eq!(
            extracted.step3_group_collapse.get("BGEE::BLOCK_A"),
            Some(&true)
        );
        assert_eq!(
            extracted.step3_group_collapse.get("BG2EE::BLOCK_C"),
            Some(&true)
        );

        // Populate the extracted state back: collapsed blocks come back per
        // tab.
        let mut ws2 = WizardState::default();
        populate_wizard_state_from_workspace(
            &extracted,
            &entry(Game::EET),
            &SettingsStore::new_default(),
            &mut ws2,
        );
        assert!(
            ws2.step3
                .bgee_collapsed_blocks
                .contains(&"BLOCK_A".to_string())
        );
        assert!(
            ws2.step3
                .bgee_collapsed_blocks
                .contains(&"BLOCK_B".to_string())
        );
        assert!(
            ws2.step3
                .bg2ee_collapsed_blocks
                .contains(&"BLOCK_C".to_string())
        );
    }

    /// A modlist swap must not bleed checks across modlists: re-populating
    /// with a different (empty) order clears the previously-checked
    /// components.
    #[test]
    fn swap_clears_prior_selection() {
        let mut ws = WizardState::default();
        ws.step2.bgee_mods = vec![mod_state(
            "EEFixPack",
            "EEFIXPACK/EEFIXPACK.TP2",
            vec![comp("0", "Core")],
        )];
        let mut a = ModlistWorkspaceState::default();
        a.order_bgee = vec![ComponentRef {
            tp2: "EEFIXPACK/EEFIXPACK.TP2".to_string(),
            id: 0,
            language: 0,
        }];
        populate_wizard_state_from_workspace(
            &a,
            &entry(Game::BGEE),
            &SettingsStore::new_default(),
            &mut ws,
        );
        assert!(ws.step2.bgee_mods[0].components[0].checked);

        // Swap to an empty modlist.
        let b = ModlistWorkspaceState::default();
        populate_wizard_state_from_workspace(
            &b,
            &entry(Game::BGEE),
            &SettingsStore::new_default(),
            &mut ws,
        );
        assert!(
            !ws.step2.bgee_mods[0].components[0].checked,
            "prior modlist's check must not bleed into the swapped-in modlist"
        );
        assert!(ws.step3.bgee_items.is_empty());
    }
