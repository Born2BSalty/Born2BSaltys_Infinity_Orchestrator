/// Synthetic component id BIO's `step3_sync` uses for the mod-header parent
/// rows. These rows are regenerated by `build_step3_items`; they are never
/// persisted (extract skips them) and never matched against the scan.
const PARENT_PLACEHOLDER_ID: &str = "__PARENT__";

