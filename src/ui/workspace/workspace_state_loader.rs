/// Write a modlist's persisted workspace state into the orchestrator's owned
/// `WizardState`. Called on workspace open / modlist swap (P6.T1 / P6.T12).
///
/// - `step1.game_install` ← `entry.game` (the per-modlist game — SPEC §5.1).
///   This write lives only in the orchestrator's in-memory `WizardState`; it
///   does **not** reach `bio_settings.json` (the snapshot masks it — see
///   `OrchestratorApp::bio_settings_snapshot`).
/// - Path / tool fields ← copied from the loaded `Step1Settings` (via
///   `sync_paths_from_settings`).
/// - Step 2 selection ← each `order_<tab>` `ComponentRef` re-marks its
///   matching scanned `Step2ComponentState` as `checked` with its position
///   as `selected_order`; the per-mod tri-state `checked` is re-derived.
/// - Step 3 order ← rebuilt from the now-marked Step 2 mods via BIO's
///   canonical `step3_sync::build_step3_items`.
/// - Step 5 ← reset to defaults (install state is per-active-install, not
///   per-workspace-load — Phase 7 owns it).
pub fn populate_wizard_state_from_workspace(
    workspace: &ModlistWorkspaceState,
    entry: &ModlistEntry,
    settings_store: &SettingsStore,
    wizard_state: &mut WizardState,
) {
    // 1. Per-modlist game (SPEC §5.1). In-memory only.
    wizard_state.step1.game_install = entry.game.to_legacy_string().to_string();

    // 2. Global paths / tools from Settings — the one-time open re-assert.
    //    This is the ONLY caller: Settings → Paths edits the shared
    //    `wizard_state.step1` directly, so no per-frame sync is needed.
    sync_paths_from_settings(settings_store, wizard_state);

    // 3. Step 2 selection reconstruction. Mark each persisted ComponentRef's
    //    scanned component `checked` + `selected_order`; then re-derive the
    //    per-mod tri-state. Components not present in the scan (mods not yet
    //    downloaded — fork / fresh modlist before Phase 7 P7.T17 binds the
    //    live pipeline) are simply not matched; the order list rebuilds from
    //    whatever IS present, which is correct.
    apply_order_to_mods(&workspace.order_bgee, &mut wizard_state.step2.bgee_mods);
    apply_order_to_mods(&workspace.order_bg2ee, &mut wizard_state.step2.bg2ee_mods);
    // IWDEE: BIO routes IWDEE installs through the BGEE mod set / tab (single
    // game). Apply IWDEE order onto the BGEE mods when this is an IWDEE
    // modlist so a persisted IWDEE selection reconstructs.
    if entry.game == Game::IWDEE {
        apply_order_to_mods(&workspace.order_iwdee, &mut wizard_state.step2.bgee_mods);
    }
    recompute_mod_checked(&mut wizard_state.step2.bgee_mods);
    recompute_mod_checked(&mut wizard_state.step2.bg2ee_mods);

    // Keep `next_selection_order` ahead of the largest restored order so the
    // next user check appends after the restored list (matches BIO's
    // contract for `selected_order`).
    let max_order = max_selected_order(&wizard_state.step2.bgee_mods)
        .max(max_selected_order(&wizard_state.step2.bg2ee_mods));
    wizard_state.step2.next_selection_order = max_order + 1;

    // 4. Step 3 order reconstruction — BIO's own canonical builder. This
    //    produces the parent-header rows + ordered component rows exactly as
    //    BIO's live Step 2→3 sync does.
    wizard_state.step3.bgee_items = step3_sync::build_step3_items(&wizard_state.step2.bgee_mods);
    wizard_state.step3.bg2ee_items = step3_sync::build_step3_items(&wizard_state.step2.bg2ee_mods);

    // 5. Step 3 group-collapse restore (this one IS a WizardState field).
    wizard_state.step3.bgee_collapsed_blocks = collapsed_blocks_for_tab(workspace, "BGEE");
    wizard_state.step3.bg2ee_collapsed_blocks = collapsed_blocks_for_tab(workspace, "BG2EE");

    // Reset the per-tab Step 3 undo/redo + drag transient state on load (per
    // the plan's open-question: the registry file is the "undo of last
    // resort"; saving the history per workspace is a post-v1 enhancement).
    wizard_state.step3.bgee_undo_stack.clear();
    wizard_state.step3.bg2ee_undo_stack.clear();
    wizard_state.step3.bgee_redo_stack.clear();
    wizard_state.step3.bg2ee_redo_stack.clear();
    wizard_state.step3.bgee_selected.clear();
    wizard_state.step3.bg2ee_selected.clear();
    wizard_state.step3.bgee_anchor = None;
    wizard_state.step3.bg2ee_anchor = None;

    // 6. Step 5 reset — install state is per-active-install, gated by
    //    Phase 7's install concurrency policy, never per-workspace-load.
    wizard_state.step5 = crate::app::state::Step5State::default();
}

/// Mark each `order` `ComponentRef` onto its matching scanned component:
/// `checked = true` and `selected_order = Some(position)`. Components present
/// in the scan but absent from `order` are explicitly unchecked + cleared so
/// a swap to a different modlist's order produces a clean Step 2 state (no
/// stale checks bleed across modlists).
fn apply_order_to_mods(order: &[ComponentRef], mods: &mut [Step2ModState]) {
    // First clear everything so a modlist swap starts from a clean slate.
    for mod_state in mods.iter_mut() {
        for component in &mut mod_state.components {
            component.checked = false;
            component.selected_order = None;
        }
    }
    for (position, item) in order.iter().enumerate() {
        let want_tp2 = item.tp2.to_ascii_uppercase();
        let want_id = item.id.to_string();
        for mod_state in mods.iter_mut() {
            if mod_state.tp_file.to_ascii_uppercase() != want_tp2 {
                continue;
            }
            for component in &mut mod_state.components {
                if component.component_id == want_id {
                    component.checked = true;
                    // `selected_order` is 1-based in BIO (`next_selection_order`
                    // starts at 1); preserve the persisted order index.
                    component.selected_order = Some(position + 1);
                }
            }
        }
    }
}

/// Re-derive each mod's tri-state `checked` flag: a mod is `checked` iff at
/// least one of its components is checked (matches BIO's mod-row marker
/// semantics, where `▣`/`◩`/`☐` is computed from the leaves).
fn recompute_mod_checked(mods: &mut [Step2ModState]) {
    for mod_state in mods.iter_mut() {
        mod_state.checked = mod_state.components.iter().any(|c| c.checked);
    }
}

fn max_selected_order(mods: &[Step2ModState]) -> usize {
    mods.iter()
        .flat_map(|m| m.components.iter())
        .filter_map(|c| c.selected_order)
        .max()
        .unwrap_or(0)
}

/// Map the workspace's flat `step3_group_collapse` map back into the
/// per-tab collapsed-block vectors. Keys are stored `<TAB>::<block_id>` so
/// the two tabs don't collide.
fn collapsed_blocks_for_tab(workspace: &ModlistWorkspaceState, tab: &str) -> Vec<String> {
    let prefix = format!("{tab}::");
    let mut out: Vec<String> = workspace
        .step3_group_collapse
        .iter()
        .filter(|(_, collapsed)| **collapsed)
        .filter_map(|(key, _)| key.strip_prefix(&prefix).map(ToString::to_string))
        .collect();
    out.sort();
    out
}

