/// Read the relevant `pub` fields out of `WizardState` to produce a
/// `ModlistWorkspaceState` for the store. The inverse of
/// `populate_wizard_state_from_workspace`.
///
/// Callers (save-draft P6.T6, nav-away P6.T15, debounce P6.T11) land in
/// Runs 2/4 — extract is implemented + unit-tested this run but ships unwired,
/// which is correct.
///
/// `prior` is the workspace state currently on disk / in memory; its
/// `expand_state` (egui-side, no `WizardState` counterpart — see module
/// header) is carried through unchanged so a save doesn't drop it.
pub fn extract_workspace_state_from_wizard(
    wizard_state: &WizardState,
    prior: &ModlistWorkspaceState,
) -> ModlistWorkspaceState {
    let order_bgee = order_from_items(&wizard_state.step3.bgee_items);
    let order_bg2ee = order_from_items(&wizard_state.step3.bg2ee_items);

    // IWDEE shares the BGEE tab/mod set in BIO. When this is an IWDEE
    // workspace mirror the BGEE-derived order into `order_iwdee` so the
    // persisted file reflects the IWDEE selection; otherwise keep whatever
    // the prior file held (non-IWDEE modlists never touch order_iwdee).
    let order_iwdee = if wizard_state.step1.game_install == "IWDEE" {
        order_bgee.clone()
    } else {
        prior.order_iwdee.clone()
    };

    let mut step3_group_collapse = prior.step3_group_collapse.clone();
    write_collapsed_blocks(
        &mut step3_group_collapse,
        "BGEE",
        &wizard_state.step3.bgee_collapsed_blocks,
    );
    write_collapsed_blocks(
        &mut step3_group_collapse,
        "BG2EE",
        &wizard_state.step3.bg2ee_collapsed_blocks,
    );

    ModlistWorkspaceState {
        order_bgee,
        order_bg2ee,
        order_iwdee,
        // Step 2 mod/parent expand state is egui frame memory, not a
        // WizardState field — carry the persisted map through unchanged.
        expand_state: prior.expand_state.clone(),
        step3_group_collapse,
        // Per-workspace prompt overrides: no orchestrator write path exists
        // yet (Step 3 prompt-edit lands via BIO's internal handlers and is
        // not yet mirrored into the workspace file in Run 1); preserve the
        // prior map so a save doesn't drop user overrides.
        prompt_overrides: prior.prompt_overrides.clone(),
        // `last_share_code` is regenerated post-install by Phase 7; carry
        // through whatever the prior file had.
        last_share_code: prior.last_share_code.clone(),
        // The dev-scan source folder (the #1 fix) has no `WizardState`
        // counterpart — it is set directly by the dev-scan trigger and
        // persisted there. Carry the prior value through unchanged so an
        // unrelated save (save-draft / debounce) never drops it.
        dev_scanned_mods_folder: prior.dev_scanned_mods_folder.clone(),
    }
}

/// Project the Step 3 ordered item list into the persisted `ComponentRef`
/// vector. Synthetic parent-header rows (`is_parent` / `__PARENT__`) are
/// skipped — `build_step3_items` regenerates them on populate.
fn order_from_items(items: &[Step3ItemState]) -> Vec<ComponentRef> {
    items
        .iter()
        .filter(|item| !item.is_parent && item.component_id != PARENT_PLACEHOLDER_ID)
        .filter_map(|item| {
            // Component ids are numeric in WeiDU; a non-numeric id here is a
            // synthetic row we don't persist.
            let id = item.component_id.trim().parse::<i64>().ok()?;
            Some(ComponentRef {
                tp2: item.tp_file.clone(),
                id,
                // TP2 LANGUAGE index: BIO's Step 2/3 model does not surface a
                // per-component language on `Step3ItemState`; the project
                // installs at the scan's resolved locale. 0 is the canonical
                // default (matches `ComponentRef::default`); a future
                // language-aware enhancement can widen this.
                language: 0,
            })
        })
        .collect()
}

/// Write the per-tab collapsed-block list into the flat persisted map under
/// the `<TAB>::` prefix. Any previously-stored keys for this tab that are no
/// longer collapsed are dropped so the map doesn't accrete stale entries.
fn write_collapsed_blocks(
    map: &mut std::collections::HashMap<String, bool>,
    tab: &str,
    collapsed: &[String],
) {
    let prefix = format!("{tab}::");
    map.retain(|key, _| !key.starts_with(&prefix));
    for block_id in collapsed {
        map.insert(format!("{prefix}{block_id}"), true);
    }
}

